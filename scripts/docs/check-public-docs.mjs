import { execFileSync } from 'node:child_process';
import { existsSync, readdirSync, readFileSync, statSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '../..');
const errors = [];
const writeMode = process.argv.includes('--write');

function repoPath(relativePath) {
  return path.join(repoRoot, relativePath);
}

function readJson(relativePath) {
  return JSON.parse(readFileSync(repoPath(relativePath), 'utf8'));
}

function readText(relativePath) {
  return readFileSync(repoPath(relativePath), 'utf8');
}

function writeText(relativePath, text) {
  writeFileSync(repoPath(relativePath), text);
}

function fail(message) {
  errors.push(message);
}

function assertExists(relativePath, label = relativePath) {
  if (!existsSync(repoPath(relativePath))) fail(`${label} is missing: ${relativePath}`);
}

function isGitIgnored(relativePath) {
  try {
    execFileSync('git', ['check-ignore', '--quiet', relativePath], {
      cwd: repoRoot,
      stdio: 'ignore',
    });
    return true;
  } catch (error) {
    if (typeof error?.status === 'number') return false;
    return false;
  }
}

function routeToFile(route) {
  if (route === '/') return 'packages/dashboard/app/page.tsx';
  const segments = route
    .split('/')
    .filter(Boolean)
    .map((segment) => (segment.startsWith('[') ? segment : segment));
  return path.posix.join('packages/dashboard/app', ...segments, 'page.tsx');
}

function buildLlmsIndex(manifest) {
  const lines = [
    '# Intercal',
    '',
    'Intercal is an open, provenance-backed temporal knowledge substrate for agents and LLM apps. It serves public docs, REST, generated OpenAPI, and MCP from https://intercal.jami.studio.',
    '',
    '## Docs',
    '',
    `- [Docs home](${manifest.baseUrl}/docs): ${manifest.title}`,
    ...manifest.pages.map(
      (page) => `- [${page.title}](${manifest.baseUrl}/docs/${page.slug}): ${page.description}`,
    ),
    '',
    '## Machine-readable surfaces',
    '',
    `- [OpenAPI](${manifest.baseUrl}${manifest.api.publicOpenapiRoute})`,
    `- [MCP](${manifest.baseUrl}${manifest.api.mcpRoute})`,
    `- [Full docs export](${manifest.baseUrl}/llms-full.txt)`,
  ];
  return `${lines.join('\n')}\n`;
}

function buildLlmsFull(manifest) {
  const sections = manifest.pages.map((page) => readText(page.source).trim()).join('\n\n---\n\n');
  return `${[
    '# Intercal',
    '',
    'Intercal is an open, provenance-backed temporal knowledge substrate for agents and LLM apps. It serves public docs, REST, generated OpenAPI, and MCP from https://intercal.jami.studio.',
    '',
    `Source exports are generated from ${manifest.pages.length} source-owned pages listed in docs/public/manifest.json.`,
    '',
    '---',
    '',
    sections,
  ].join('\n')}\n`;
}

function buildDashboardGeneratedModule(manifest) {
  const markdownBySlug = Object.fromEntries(
    manifest.pages.map((page) => [page.slug, readText(page.source).trim()]),
  );
  return `// AUTO-GENERATED by scripts/docs/check-public-docs.mjs --write. Do not edit.
import type { PublicDocPage, PublicDocsManifest } from './public-docs';

export const publicDocsManifest = ${JSON.stringify(manifest, null, 2)} as const satisfies PublicDocsManifest;

export const publicDocMarkdown = ${JSON.stringify(markdownBySlug, null, 2)} as const satisfies Record<PublicDocPage['slug'], string>;
`;
}

function flattenMintlifyPages(node, pages = []) {
  if (typeof node === 'string') {
    pages.push(node);
    return pages;
  }
  if (Array.isArray(node)) {
    for (const item of node) flattenMintlifyPages(item, pages);
    return pages;
  }
  if (!node || typeof node !== 'object') return pages;
  for (const value of Object.values(node)) flattenMintlifyPages(value, pages);
  return pages;
}

function markdownLinks(text) {
  return [...text.matchAll(/\[[^\]]+\]\(([^)]+)\)/g)].map((match) => match[1]);
}

function dashboardRouteFromSegments(segments) {
  const routeSegments = segments.filter(
    (segment) => !(segment.startsWith('(') && segment.endsWith(')')),
  );
  return routeSegments.length ? `/${routeSegments.join('/')}` : '/';
}

function scanDashboardPageRoutes(dir = repoPath('packages/dashboard/app'), segments = []) {
  const routes = [];
  for (const entry of readdirSync(dir)) {
    const fullPath = path.join(dir, entry);
    if (statSync(fullPath).isDirectory()) {
      routes.push(...scanDashboardPageRoutes(fullPath, [...segments, entry]));
      continue;
    }
    if (entry === 'page.tsx') routes.push(dashboardRouteFromSegments(segments));
  }
  return routes.sort();
}

function scanPublicDocSources(dir = repoPath('docs/public/pages')) {
  const sources = [];
  for (const entry of readdirSync(dir)) {
    const fullPath = path.join(dir, entry);
    if (statSync(fullPath).isDirectory()) {
      sources.push(...scanPublicDocSources(fullPath));
      continue;
    }
    if (entry.endsWith('.md')) {
      sources.push(path.relative(repoRoot, fullPath).replaceAll(path.sep, '/'));
    }
  }
  return sources.sort();
}

function assertSameSet(label, actual, expected) {
  const actualSet = new Set(actual);
  const expectedSet = new Set(expected);
  for (const item of expectedSet) {
    if (!actualSet.has(item)) fail(`${label} missing: ${item}`);
  }
  for (const item of actualSet) {
    if (!expectedSet.has(item)) fail(`${label} unexpected: ${item}`);
  }
}

function docsJsonAssetPaths(node, assets = []) {
  if (typeof node === 'string') {
    if (node.startsWith('/') && /\.(svg|png|jpe?g|webp|ico)$/i.test(node)) {
      assets.push(node);
    }
    return assets;
  }
  if (Array.isArray(node)) {
    for (const item of node) docsJsonAssetPaths(item, assets);
    return assets;
  }
  if (!node || typeof node !== 'object') return assets;
  for (const value of Object.values(node)) docsJsonAssetPaths(value, assets);
  return assets;
}

function openApiParameterNames(openapiDocument, operation) {
  return new Set(
    (operation.parameters ?? []).map((parameter) => {
      if (parameter.$ref) {
        const key = parameter.$ref.replace('#/components/parameters/', '');
        return openapiDocument.components?.parameters?.[key]?.name;
      }
      return parameter.name;
    }),
  );
}

function assertRestExamplesMatchOpenApi(openapiDocument, examplesMarkdown) {
  const requestLines = [
    ...examplesMarkdown.matchAll(/^(GET|POST|PUT|PATCH|DELETE)\s+\/api([^\s]+)$/gm),
  ];
  if (requestLines.length === 0) fail('examples page must include at least one REST request line');

  for (const match of requestLines) {
    const method = match[1]?.toLowerCase();
    const route = match[2] ?? '';
    const [apiPath, query = ''] = route.split('?');
    const operation = openapiDocument.paths?.[apiPath]?.[method];
    if (!operation) {
      fail(`examples page REST request is not in OpenAPI: ${match[1]} /api${route}`);
      continue;
    }
    const parameterNames = openApiParameterNames(openapiDocument, operation);
    for (const key of new URLSearchParams(query).keys()) {
      if (!parameterNames.has(key)) {
        fail(`examples page REST request uses unknown query parameter "${key}" for ${apiPath}`);
      }
    }
  }
}

function sharedV1ToolNames() {
  const sharedSource = readText('packages/shared/src/index.ts');
  return [...sharedSource.matchAll(/name:\s+'([^']+)'/g)].map((match) => match[1]).sort();
}

const manifest = readJson('docs/public/manifest.json');
const docsJson = readJson('docs.json');
const openapi = readJson(manifest.api.openapiSource);
const expectedLlmsIndex = buildLlmsIndex(manifest);
const expectedLlmsFull = buildLlmsFull(manifest);
const expectedDashboardModule = buildDashboardGeneratedModule(manifest);
const requiredDocSlugs = [
  'introduction',
  'concepts',
  'quickstart',
  'mcp',
  'rest',
  'sdk',
  'authentication',
  'examples',
  'source-policy',
  'provenance',
  'corpus-coverage',
  'operations-transparency',
];

if (writeMode) {
  writeText(manifest.exports.index, expectedLlmsIndex);
  writeText(manifest.exports.full, expectedLlmsFull);
  writeText('packages/dashboard/lib/public-docs.gen.ts', expectedDashboardModule);
}

const slugs = new Set();
for (const page of manifest.pages) {
  if (slugs.has(page.slug)) fail(`duplicate docs slug: ${page.slug}`);
  slugs.add(page.slug);
  if (path.basename(page.source, '.md') !== page.slug) {
    fail(`${page.source} basename must match docs slug "${page.slug}"`);
  }
  assertExists(page.source, `docs page ${page.slug}`);
  const markdown = readText(page.source);
  if (!markdown.startsWith(`# ${page.title}\n`)) {
    fail(`${page.source} must start with "# ${page.title}"`);
  }
  for (const href of markdownLinks(markdown)) {
    if (/^https?:\/\//.test(href) || href.startsWith('mailto:')) continue;
    if (href.startsWith('/docs/')) {
      const slug = href.replace('/docs/', '').replace(/\/$/, '');
      if (!slugs.has(slug) && !manifest.pages.some((item) => item.slug === slug)) {
        fail(`${page.source} links to unknown docs slug: ${href}`);
      }
      continue;
    }
    if (href.startsWith('/api/') || href === '/llms.txt' || href === '/llms-full.txt') continue;
    assertExists(path.posix.normalize(path.posix.join(path.posix.dirname(page.source), href)));
  }
}

assertSameSet(
  'docs/public/manifest.json page sources',
  manifest.pages.map((page) => page.source).sort(),
  scanPublicDocSources(),
);
assertSameSet('docs/public/manifest.json slugs', [...slugs].sort(), requiredDocSlugs);
assertSameSet(
  'docs/public/manifest.json dashboardRoutes',
  manifest.dashboardRoutes,
  scanDashboardPageRoutes(),
);

for (const route of manifest.dashboardRoutes) {
  const routeFile = routeToFile(route);
  assertExists(routeFile, `dashboard route ${route}`);
  if (isGitIgnored(routeFile)) {
    fail(`dashboard route ${route} is ignored by git: ${routeFile}`);
  }
}

assertExists('packages/dashboard/app/llms.txt/route.ts', 'llms.txt route');
assertExists('packages/dashboard/app/llms-full.txt/route.ts', 'llms-full.txt route');
assertExists('packages/dashboard/app/api/[[...route]]/route.ts', 'OpenAPI route mount');
assertExists('packages/dashboard/app/api/mcp/route.ts', 'MCP route mount');

if (openapi.openapi !== '3.1.0') fail('OpenAPI document must be 3.1.0');
for (const apiPath of [
  '/v1/delta',
  '/v1/entity',
  '/v1/evidence',
  '/v1/claims/verify',
  '/v1/sources',
  '/v1/freshness',
]) {
  if (!openapi.paths?.[apiPath]) fail(`OpenAPI path missing: ${apiPath}`);
}

const docsPages = manifest.pages.map((page) => page.source.replace(/\.md$/, ''));
const mintlifyPages = new Set(flattenMintlifyPages(docsJson.navigation?.groups ?? []));
for (const page of docsPages) {
  if (!mintlifyPages.has(page)) fail(`docs.json navigation missing page: ${page}`);
}
if (!readText('docs.json').includes(manifest.api.openapiSource)) {
  fail(`docs.json must reference generated OpenAPI source: ${manifest.api.openapiSource}`);
}
if (!docsJson.navigation?.groups?.some((group) => group?.openapi === manifest.api.openapiSource)) {
  fail(`docs.json navigation must expose generated OpenAPI source: ${manifest.api.openapiSource}`);
}
for (const assetPath of docsJsonAssetPaths(docsJson)) {
  const relativeAssetPath = assetPath.replace(/^\/+/, '');
  if (!existsSync(repoPath(relativeAssetPath)) || !statSync(repoPath(relativeAssetPath)).isFile()) {
    fail(`docs.json references missing asset: ${assetPath}`);
  }
}

const rest = readText('docs/public/pages/rest.md');
for (const apiPath of Object.keys(openapi.paths ?? {}).sort()) {
  if (!rest.includes(apiPath)) fail(`REST docs missing OpenAPI path: ${apiPath}`);
}

const examples = readText('docs/public/pages/examples.md');
assertRestExamplesMatchOpenApi(openapi, examples);
for (const required of [
  'GET /api/v1/delta',
  'GET /api/v1/claims/verify',
  'client.getDelta',
  'client.verifyClaim',
  '"name": "get_delta"',
  '"name": "verify_claim"',
]) {
  if (!examples.includes(required)) fail(`examples page missing checked example: ${required}`);
}

const mcp = readText('docs/public/pages/mcp.md');
for (const toolName of sharedV1ToolNames()) {
  if (!mcp.includes(toolName)) fail(`MCP docs missing tool: ${toolName}`);
}

if (readText(manifest.exports.index) !== expectedLlmsIndex) {
  fail(`${manifest.exports.index} is not in sync with docs/public/manifest.json`);
}
if (readText(manifest.exports.full) !== expectedLlmsFull) {
  fail(`${manifest.exports.full} is not in sync with docs/public/pages`);
}
if (readText('packages/dashboard/lib/public-docs.gen.ts') !== expectedDashboardModule) {
  fail('packages/dashboard/lib/public-docs.gen.ts is not in sync with docs/public source');
}

if (errors.length) {
  console.error(errors.map((error) => `- ${error}`).join('\n'));
  process.exit(1);
}

console.log(
  `public docs check passed: ${manifest.pages.length} pages, ${manifest.dashboardRoutes.length} dashboard routes, ${Object.keys(openapi.paths ?? {}).length} OpenAPI paths`,
);
