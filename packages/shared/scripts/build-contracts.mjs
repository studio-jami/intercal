#!/usr/bin/env node
// Compile the TypeSpec contract to OpenAPI 3.1 + JSON Schema, then generate TS types and
// (if uv is available) Pydantic v2 models. TypeSpec is the single source; everything else
// is generated and must not be hand-edited.

import { spawnSync } from 'node:child_process';
import { existsSync, mkdirSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const pkgRoot = resolve(here, '..');
const repoRoot = resolve(pkgRoot, '..', '..');

function commandFor(cmd, args) {
  if (cmd === 'pnpm' && process.env.npm_execpath) {
    return { cmd: process.execPath, args: [process.env.npm_execpath, ...args] };
  }
  return { cmd, args };
}

function run(cmd, args, opts = {}) {
  const printable = `${cmd} ${args.join(' ')}`;
  console.log(`\n$ ${printable}`);
  const resolved = commandFor(cmd, args);
  const r = spawnSync(resolved.cmd, resolved.args, { stdio: 'inherit', cwd: pkgRoot, ...opts });
  if (r.error) {
    console.error(`[contracts] failed to start ${cmd}: ${r.error.message}`);
  }
  return r.status ?? 1;
}

function sortJson(value) {
  if (Array.isArray(value)) return value.map(sortJson);
  if (value && typeof value === 'object') {
    return Object.fromEntries(
      Object.entries(value)
        .sort(([a], [b]) => a.localeCompare(b))
        .map(([key, child]) => [key, sortJson(child)]),
    );
  }
  return value;
}

// 1) TypeSpec -> OpenAPI 3.1 + JSON Schema (emitters configured in tspconfig.yaml)
if (run('pnpm', ['exec', 'tsp', 'compile', 'typespec/main.tsp']) !== 0) {
  console.error('[contracts] TypeSpec compile failed.');
  process.exit(1);
}

const openapiJson = join(pkgRoot, 'generated', 'openapi', 'openapi.json');
if (!existsSync(openapiJson)) {
  console.error(`[contracts] expected ${openapiJson} after compile — not found.`);
  process.exit(1);
}

// 2) OpenAPI -> TypeScript types (committed under src so tsc compiles them)
mkdirSync(join(pkgRoot, 'src', 'generated'), { recursive: true });
if (
  run('pnpm', [
    'exec',
    'openapi-typescript',
    'generated/openapi/openapi.json',
    '-o',
    'src/generated/types.gen.ts',
  ]) !== 0
) {
  console.error('[contracts] openapi-typescript failed.');
  process.exit(1);
}

// 2b) Embed the generated artifacts as a TS module so they need no filesystem at runtime
// (works in bundlers, Node, and edge — not just on a server with disk access).
{
  const { readFileSync, readdirSync, writeFileSync } = await import('node:fs');
  const openapi = sortJson(JSON.parse(readFileSync(openapiJson, 'utf8')));
  const schemaDir = join(pkgRoot, 'generated', 'json-schema');
  const schemas = {};
  for (const file of readdirSync(schemaDir)
    .filter((f) => f.endsWith('.json'))
    .sort()) {
    schemas[file.replace(/\.json$/, '')] = sortJson(
      JSON.parse(readFileSync(join(schemaDir, file), 'utf8')),
    );
  }
  const out = `// AUTO-GENERATED by scripts/build-contracts.mjs — do not edit.\n\nexport const openapiDocument = ${JSON.stringify(openapi)} as const;\n\nexport const jsonSchemas: Record<string, Record<string, unknown>> = ${JSON.stringify(schemas)};\n`;
  writeFileSync(join(pkgRoot, 'src', 'generated', 'artifacts.gen.ts'), out);
  console.log('[contracts] embedded artifacts.gen.ts');
}

// 3) OpenAPI -> Pydantic v2 (optional; needs uv. TS-only contributors can skip.)
const pyOut = join(
  repoRoot,
  'services',
  'shared',
  'src',
  'intercal_shared',
  'contracts',
  'models.py',
);
if (existsSync(dirname(pyOut))) {
  const code = run(
    'uv',
    [
      'run',
      '--with',
      'datamodel-code-generator',
      'datamodel-codegen',
      '--input',
      'packages/shared/generated/openapi/openapi.json',
      '--input-file-type',
      'openapi',
      '--output',
      'services/shared/src/intercal_shared/contracts/models.py',
      '--output-model-type',
      'pydantic_v2.BaseModel',
      '--target-python-version',
      '3.12',
      '--use-standard-collections',
      '--use-union-operator',
      '--disable-timestamp',
      '--formatters',
      'black',
      'isort',
    ],
    { cwd: repoRoot, env: { ...process.env, UV_LINK_MODE: process.env.UV_LINK_MODE ?? 'copy' } },
  );
  if (code !== 0) {
    console.warn(
      '[contracts] Pydantic generation skipped/failed (is uv installed? run `uv sync`). ' +
        'TypeScript contracts are still up to date.',
    );
  } else {
    console.log('[contracts] Pydantic models generated.');
  }
} else {
  console.warn('[contracts] services/shared not present yet — skipping Pydantic generation.');
}

console.log('\n[contracts] build complete.');
